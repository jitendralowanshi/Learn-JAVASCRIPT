let a = 10;
let b = 5;

// ADDITION 
let add = a + b;
console.log(add);

// SUBSTRACTION 
let subs = a - b;
console.log(subs);

// MULTIPLICATION 
let mul = a * b;
console.log(mul);

// DIVIDE
let divide = a/b;
console.log(divide);

// MODULO  remainder
let mod = a % b;
console.log(mod);

// EXPONENTION POWER
let pow = a**b;
